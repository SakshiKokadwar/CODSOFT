#include<iostream>
using namespace std;
// << this is called insertion operator
// >>this is called as abstraction operator
int main(){
    int num1, num2;
    cout<<"Enter the value of num1:";
    cin>>num1;
    cout<<"Enter the value of num2:";
    cin>>num2;
    cout<<"sum of num1 and num2 is:"<<num1 + num2;
    return 0;
}